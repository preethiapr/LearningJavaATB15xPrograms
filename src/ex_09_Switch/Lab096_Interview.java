package ex_09_Switch;

public class Lab096_Interview {
    static void main(String[] args) {
        char code = 'C';
        switch (code){
            default:
                System.out.println("Hellooooo....");
                break;
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
        }
    }
}
