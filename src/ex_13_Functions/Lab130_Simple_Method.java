package ex_13_Functions;

public class Lab130_Simple_Method {
    static void main(String[] args) {

    }

    static void non_return_function() {
        System.out.println("Print something no return or return type");
    }

    static int return_int() {
        System.out.println("Print something integer return type");
        return 10;
    }

    static boolean return_boolean() {
        System.out.println("Print something boolean return type");
        return true;
    }

    static float return_pi_value() {
        System.out.println("Print something float return type");
        return 3.14F;
    }

}
