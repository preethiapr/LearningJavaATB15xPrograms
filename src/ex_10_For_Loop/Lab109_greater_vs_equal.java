package ex_10_For_Loop;

public class Lab109_greater_vs_equal {
    static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
        for (int j = 1; j <= 10; j++) {
            System.out.println(j);
        }
        for (int k = 0; k<10; k++){
            System.out.println(k);
        }
    for (int l = 0; l<10; ++l) {
        System.out.println(l);
    }
    }
}